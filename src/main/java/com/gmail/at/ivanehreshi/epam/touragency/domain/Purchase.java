package com.gmail.at.ivanehreshi.epam.touragency.domain;

public class Purchase {
    private Long id;
    private User user;
    private Tour tour;

    public Purchase(Long id) {
        this.id = id;
    }

    public Tour getTour() {
        return tour;
    }

    public void setTour(Tour tour) {
        this.tour = tour;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }
}
